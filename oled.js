Screen.prototype.draw = function(x, y, x0, y0, bmp) {
    var w = x0 - x;
    for (var i = 0; i < bmp.length; i++) {
        var _y, _x = i % w + x, _p = bmp[i];

        for (var j = 0; j < 8; j++) {
            _y = (y + Math.floor(i / w)) * 8 + j;
            if (_y >= y0 * 8) {
                return;
            }

            if (_y >= 16) {
                this.context.fillStyle = '#00ffff';
            } else {
                this.context.fillStyle = '#ffc000';
            }

            if (_p % 2) {
                this.context.fillRect(_x * this.scale, _y * this.scale, this.scale, this.scale);
            } else {
                this.context.clearRect(_x * this.scale, _y * this.scale, this.scale, this.scale);
            }
            _p >>= 1;
        }
    }
}

Screen.prototype.printChar = function(x, y, char) {
    var charArray = ' !"#$%&\'()*+,-./0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_`abcdefghijklmnopqrstuvwxyz{|}~';
    index = charArray.indexOf(char);
    if (index === -1) {
        index = 31;
    }

    var pixel = this.font[index];
    this.draw(x * 8, y * 2, x * 8 + 8, y * 2 + 2, pixel);
}

Screen.prototype.print = function(line, str, wrap) {
    if (typeof(line) !== 'number') {
        str = line;
        wrap = str;
        line = 0;
    }

    this.context.clearRect(0, line * 16 * this.scale, 128 * this.scale, (line + 1) * 16 * this.scale);

    var c = 0;
    for (var i = 0; i < str.length; i++) {
        if (str[i] === '\n' || c >= 16 && wrap) {
            line++;
            this.context.clearRect(0, line * 16 * this.scale, 128 * this.scale, (line + 1) * 16 * this.scale);
            c = 0;
        } else {
            if (c >= 16 || line >= 4) {
                continue;
            }
            this.printChar(c++, line, str[i]);
        }
    }
}

Screen.prototype.clear = function() {
    this.context.clearRect(0, 0, 128 * this.scale, 64 * this.scale);
}